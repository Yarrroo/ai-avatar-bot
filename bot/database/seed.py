async def seed_avatars(session: AsyncSession) -> None:
    """Upsert seed avatars. Updates prompts on restart if changed."""
    for avatar_data in SEED_AVATARS:
        stmt = (
            pg_insert(Avatar)
            .values(**avatar_data)
            .on_conflict_do_update(
                index_elements=["id"],
                set_={
                    "system_prompt": avatar_data["system_prompt"],
                    "description": avatar_data["description"],
                    "greeting": avatar_data["greeting"],
                },
            )
        )
        await session.execute(stmt)
